# =====================================================
# Django Import
# =====================================================

from django.db import models


# =====================================================
# Core Model Import
# =====================================================

from core.models import BaseModel


# =====================================================
# Product Model
# =====================================================

class Product(BaseModel):

    # =================================================
    # Product Name
    # =================================================

    name = models.CharField(

        max_length=200

    )

    # =================================================
    # Product SKU
    # =================================================

    sku = models.CharField(

        max_length=100,

        unique=True

    )

    # =================================================
    # Product Price
    # =================================================

    price = models.DecimalField(

        max_digits=10,

        decimal_places=2,

        default=0

    )

    # =================================================
    # Product Quantity
    # =================================================

    quantity = models.PositiveIntegerField(

        default=0

    )

    # =================================================
    # Admin Display
    # =================================================

    def __str__(self):

        return self.name